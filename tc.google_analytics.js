(function(window, $) {
	
	var NI = window.NI;
	
	function GoogleAnalytics(options) {
		var o;
		
		o = $.extend({
			account:null
		}, options);
		
		if(!o.account){
			return;
		}
		if(window._gat){
			this.tracker = window._gat._createTracker(o.account);
		}
		
		
	}

//::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::
		
	NI.GoogleAnalytics = GoogleAnalytics;
	
}(this, this.jQuery));